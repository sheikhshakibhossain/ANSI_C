package lab2;

public class NewsPaper {
    String language;
    String url;
    int numOfPages;
    String headQuaterLocation;
    String fontFace;

    public NewsPaper(String language, String url, int numOfPages, String headQuaterLocation, String fontFace) {
        this.language = language;
        this.url = url;
        this.numOfPages = numOfPages;
        this.headQuaterLocation = headQuaterLocation;
        this.fontFace = fontFace;
    }
}
